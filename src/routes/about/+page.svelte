

<h1>Über uns</h1> 

<p>
  Willkommen bei <strong>Leseverwaltung</strong> –
  deiner persönlichen Plattform, um Bücher zu erfassen, zu verwalten und wiederzufinden.
</p>

<p>
  Ob Roman, Sachbuch oder Klassiker:
  Mit unserer App behältst du stets den Überblick über deine gelesenen Werke,
  Wunschlisten und Lieblingsautoren.
</p>

<p>
  Unser Ziel ist es, das Lesen nicht nur zu dokumentieren, sondern auch zu inspirieren.
  Mit einer einfachen Oberfläche, einer Suchfunktion und der Möglichkeit, neue Bücher zu entdecken,
  möchten wir deine Leseerfahrung digital begleiten.
</p>

<p>
  Dieses Projekt wurde im Rahmen einer Studienarbeit entwickelt
  und verbindet moderne Webtechnologie (SvelteKit & MongoDB) mit der Liebe zur Literatur.
</p>

<p>
  Viel Spaß beim Verwalten und Entdecken deiner Bücher!
</p>
